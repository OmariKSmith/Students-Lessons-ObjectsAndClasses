 /*
 -                                    What are accessor and mutator methods?

 `                              Accessors (also called getters) are methods that retrieve
 `                              the value of a field. They let other parts of your
 `                              code read the data without directly accessing the field.
 `
 `                              Mutators (also called setters) are methods that modify or set the value
 `                              of a field, allowing controlled updates to the data.


-                                       Why use accessors and mutators?
`
`                               Accessors and Mutators allow us to apply rules on how class
`                               variables values are read and updated.
  */
